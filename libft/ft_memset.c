#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char*)b;
	while (i < len)
	{
		ptr[i] = (char)c;
		i++;
	}
	return (b);
}
