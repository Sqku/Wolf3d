#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	int		length;
	char	*ret;

	if (s == NULL || f == NULL)
		return (NULL);
	length = ft_strlen(s);
	if ((ret = (char *)malloc(sizeof(char) * length + 1)) == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ret[i] = f(s[i]);
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
